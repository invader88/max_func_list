numbers = input("Enter list of numbers separating them with commas: ")
def my_max(num):
    num = num.split(',')
    int_list = [] 
    for i in num:
        int_list.append(int(i))
    max = int_list[0]
    for j in range(len(int_list)):
        if int_list[j]>max:
            max = int_list[j]
    print(max)
    
my_max(numbers)

        
